    public function getLegendFormat()
    {
        return $this->container['legendFormat'];
    }

    /**
     * Sets legendFormat
     * @param string $legendFormat
     * @return $this
     */
    public function setLegendFormat($legendFormat)
    {
        $this->container['legendFormat'] = $legendFormat;

        return $this;
